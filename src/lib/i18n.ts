export type TKey = keyof typeof translations.es

/**
 * Look up a UI string. Optional `vars` does `{name}` placeholder
 * substitution — used by the geo-feed strings that interpolate the
 * city name (e.g. `feed_h1` = 'Anuncios destacados en {city}').
 */
export function t(lang: Lang, key: TKey, vars?: Record<string, string>): string {
  const dict = translations[lang] as Record<string, string>
  let s = dict[key] ?? (translations.es as Record<string, string>)[key] ?? key
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      s = s.split('{' + k + '}').join(v)
    }
  }
  return s
}

